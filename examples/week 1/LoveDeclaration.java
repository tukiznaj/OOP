public class LoveDeclaration {
    public static void main(String[] args) {
        // increasing
        for (int i = 0; i < 5; i++) {
            System.out.println("I Love Java");
        }
        System.out.println("-----");

        // decreasing index
        for (int i = 5; i > 0; i--) {
            System.out.println("I Love Java");
        }

        // 2 4 6 8 10
        for (int i = 1; i < 6; i++) {
            System.out.println(i * 2);
        }

    }
}